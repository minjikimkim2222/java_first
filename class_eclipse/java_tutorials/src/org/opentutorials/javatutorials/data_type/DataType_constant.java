package org.opentutorials.javatutorials.data_type;

public class DataType_constant {
	public static void main(String[] args)
	{
	// 1. 실수의 표현 
		double a = 2.2;
		
		float b = 2.2f;
		float b2 = 2.2F;
		// float형은 double과 달리, 2.2가 float이라는 걸 분명히 명시해야 함.
		
	// 2. 정수의 표현
		long l = 2147483648l;
		
	}
}
