package org.opentutorials.javatutorials.data_type;

public class DataType_variable {

	public static void main(String[] args) {
		int a = 10;
		System.out.println("정수형은 int를 쓰도록 하자 " +a);
		
		double b = 152.2;
		System.out.println("실수형은 double을 쓰도록 하자 " + b);
		
		char c = 65;
		char c2 = 'A';
		System.out.println("문자는 char을 쓴다 " + c);
		System.out.println("c2 : " + c2);
		
		String str = "ABCD#FG";
		System.out.println(str);

	}

}
